// Declare the variables
let num1 = 31;
let num2 = 2;
let num3 = 10;
let num4 = 6;

// Multiply num1 and num2 to get 62
let multiply = num1 * num2;

// Generate a random integer greater than 0
let random = Math.floor(Math.random() * 100) + 1; // Generates a number between 1 and 100

// Calculate the remainder of num3 divided by num4 (should be 4)
let mod = num3 % num4;

// Find the maximum number in a set (should be 20)
let max = Math.max(5, 10, 15, 20);

console.log(multiply); // Expected output: 62
console.log(random);   // Expected output: Random integer > 0
console.log(mod);      // Expected output: 4
console.log(max);      // Expected output: 20
